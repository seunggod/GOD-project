  PROCEDURE PROC_DELETECMT
  ( IN_TBC_NUM IN NUMBER,
    IN_TB_NUM   IN NUMBER,
    CURRENTPAGE IN NUMBER,
    DPP         IN NUMBER,
    O_CUR       OUT SYS_REFCURSOR
  ) ;
  
