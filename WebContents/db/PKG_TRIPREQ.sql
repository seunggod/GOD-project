    IN_TB_NUM   IN NUMBER,
    IN_TBC_CONT IN VARCHAR2,
    CURRENTPAGE IN NUMBER,
    DPP         IN NUMBER,
    O_CUR       OUT SYS_REFCURSOR
  );
