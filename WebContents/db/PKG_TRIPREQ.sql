  --게시물 가져오기
  PROCEDURE PROC_GETBOARD
  ( IN_BOARDNUM IN NUMBER,
    O_CUR       OUT SYS_REFCURSOR);
  --게시물 좋아요
  PROCEDURE PROC_LIKEUPDATE
  ( IN_MEM_NUM IN NUMBER,
    IN_TB_NUM  IN NUMBER,
    LIKECNT    OUT NUMBER
  );
  --게시물 좋아요 취소
   PROCEDURE PROC_LIKEDELETE
  ( IN_MEM_NUM IN NUMBER,
    IN_TB_NUM  IN NUMBER,
    LIKECNT    OUT NUMBER
  );
  --댓글 작성
  PROCEDURE PROC_INSERTCMT
